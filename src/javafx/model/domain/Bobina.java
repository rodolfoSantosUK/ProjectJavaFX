package javafx.model.domain;

import java.math.BigDecimal;

public class Bobina {
    
    private Long idBobina;
    
    private String cliente;
    
    private String turno;
    
    private String tipo;
    
    private BigDecimal peso;
    
    private String bobina;

    
    
    /**
     * @return the idBobina
     */
    public Long getIdBobina() {
        return idBobina;
    }

    /**
     * @param idBobina the idBobina to set
     */
    public void setIdBobina(Long idBobina) {
        this.idBobina = idBobina;
    }

    /**
     * @return the cliente
     */
    public String getCliente() {
        return cliente;
    }

    /**
     * @param cliente the cliente to set
     */
    public void setCliente(String cliente) {
        this.cliente = cliente;
    }

    /**
     * @return the turno
     */
    public String getTurno() {
        return turno;
    }

    /**
     * @param turno the turno to set
     */
    public void setTurno(String turno) {
        this.turno = turno;
    }

    /**
     * @return the peso
     */
    public BigDecimal getPeso() {
        return peso;
    }

    /**
     * @param peso the peso to set
     */
    public void setPeso(BigDecimal peso) {
        this.peso = peso;
    }

    /**
     * @return the bobina
     */
    public String getBobina() {
        return bobina;
    }

    /**
     * @param bobina the bobina to set
     */
    public void setBobina(String bobina) {
        this.bobina = bobina;
    }

    /**
     * @return the tipo
     */
    public String getTipo() {
        return tipo;
    }

    /**
     * @param tipo the tipo to set
     */
    public void setTipo(String tipo) {
        this.tipo = tipo;
    }
    
    
    
    
}
